#[cfg(test)]
mod tests {
    use super::*;
    use crate::kernel::note_id::NoteId;
    use crate::kernel::note_number::NoteNumber;
    use crate::kernel::velocity::Velocity;

    // ── Helpers ───────────────────────────────────────────────────────────────

    fn note_id(v: u32) -> NoteId {
        NoteId::new(v)
    }

    fn note_num(v: u8) -> NoteNumber {
        NoteNumber::try_new(v).expect("valid note number")
    }

    fn vel(v: f64) -> Velocity {
        Velocity::try_new(v).expect("valid velocity")
    }

    // ── Construction ──────────────────────────────────────────────────────────

    #[test]
    fn new_allocator_has_correct_voice_count() {
        let alloc = VoiceAllocator::new(4);
        assert_eq!(alloc.voice_count(), 4);
    }

    #[test]
    fn new_allocator_caps_at_max_voices() {
        let alloc = VoiceAllocator::new(MAX_VOICES + 100);
        assert_eq!(alloc.voice_count(), MAX_VOICES);
    }

    #[test]
    #[should_panic(expected = "num_voices must be at least 1")]
    fn new_allocator_panics_on_zero_voices() {
        let _ = VoiceAllocator::new(0);
    }

    // ── NoteOn ────────────────────────────────────────────────────────────────

    #[test]
    fn note_on_emits_voice_activated() {
        let mut alloc = VoiceAllocator::new(4);
        let events = alloc.note_on(note_id(1), note_num(69), vel(0.8));
        assert!(events.iter().any(|e| matches!(
            e,
            AllocatorEvent::VoiceActivated { note_id, .. } if *note_id == NoteId::new(1)
        )));
    }

    #[test]
    fn note_on_increments_active_count() {
        let mut alloc = VoiceAllocator::new(4);
        assert_eq!(alloc.active_count(), 0);
        alloc.note_on(note_id(1), note_num(60), vel(0.5));
        assert_eq!(alloc.active_count(), 1);
    }

    #[test]
    fn note_on_fills_all_voices() {
        let mut alloc = VoiceAllocator::new(4);
        for i in 0..4 {
            alloc.note_on(note_id(i), note_num(60 + i as u8), vel(0.5));
        }
        assert_eq!(alloc.active_count(), 4);
    }

    // ── NoteOff ───────────────────────────────────────────────────────────────

    #[test]
    fn note_off_emits_voice_released() {
        let mut alloc = VoiceAllocator::new(4);
        alloc.note_on(note_id(1), note_num(69), vel(0.8));
        let result = alloc.note_off(note_id(1));
        assert!(
            matches!(result, Ok(AllocatorEvent::VoiceReleased { note_id, .. }) if note_id == NoteId::new(1))
        );
    }

    #[test]
    fn note_off_unknown_note_id_returns_error() {
        let mut alloc = VoiceAllocator::new(4);
        let result = alloc.note_off(note_id(99));
        assert!(matches!(result, Err(AllocatorError::NoteNotFound(_))));
    }

    #[test]
    fn note_off_for_unknown_id_returns_error_regardless_of_state() {
        let mut alloc = VoiceAllocator::new(4);
        alloc.note_on(note_id(1), note_num(69), vel(0.8));
        let _ = alloc.note_off(note_id(1));
        // note_off for a completely different note_id should fail.
        let result = alloc.note_off(note_id(999));
        assert!(result.is_err());
    }

    // ── Voice stealing ────────────────────────────────────────────────────────

    #[test]
    fn note_on_steals_voice_when_pool_full() {
        let mut alloc = VoiceAllocator::new(2);
        alloc.note_on(note_id(1), note_num(60), vel(0.5));
        alloc.note_on(note_id(2), note_num(62), vel(0.5));

        // Pool is full; this should steal
        let events = alloc.note_on(note_id(3), note_num(64), vel(0.5));
        let has_stolen = events
            .iter()
            .any(|e| matches!(e, AllocatorEvent::VoiceStolen { .. }));
        assert!(has_stolen, "expected a VoiceStolen event when pool is full");
    }

    #[test]
    fn stolen_event_carries_correct_note_ids() {
        let mut alloc = VoiceAllocator::new(1);
        alloc.note_on(note_id(10), note_num(60), vel(0.5));

        let events = alloc.note_on(note_id(20), note_num(62), vel(0.7));
        let stolen = events.iter().find_map(|e| {
            if let AllocatorEvent::VoiceStolen {
                old_note_id,
                new_note_id,
                ..
            } = e
            {
                Some((*old_note_id, *new_note_id))
            } else {
                None
            }
        });
        assert_eq!(stolen, Some((NoteId::new(10), NoteId::new(20))));
    }

    // ── Render ────────────────────────────────────────────────────────────────

    #[test]
    fn render_sample_returns_silence_when_no_voices_active() {
        let mut alloc = VoiceAllocator::new(4);
        let (sample, events) = alloc.render_sample(44100.0, 0.0);
        assert_eq!(sample, 0.0_f32);
        assert!(events.is_empty());
    }

    #[test]
    fn render_sample_emits_voice_finished_after_release() {
        use crate::synth::amp_envelope_config::AmpEnvelopeConfig;
        use crate::synth::voice::Voice;

        // Use a very fast envelope (1ms attack, 1ms decay, 0.8 sustain, 10ms release)
        // so the test completes in a small number of samples.
        let cfg = AmpEnvelopeConfig::try_new(0.001, 0.001, 0.8, 0.01).unwrap();

        // Build the allocator manually with a fast-envelope voice.
        let voice = Voice::with_config(cfg);
        // Use the public API but override the voice via a single-voice pool.
        // Since VoiceAllocator::new always uses Voice::new(), we test via
        // a helper that exercises the rendering path with enough samples.
        let mut alloc = VoiceAllocator::new(1);
        // We can't inject a custom config via VoiceAllocator::new, so we
        // render enough samples for the default 300ms release (0.3s * 44100 ≈ 13230 samples).
        alloc.note_on(note_id(1), note_num(69), vel(1.0));

        // Drive through attack + decay + sustain (default: 10ms + 100ms = 110ms ≈ 4851 samples).
        for _ in 0..6000 {
            alloc.render_sample(44100.0, 0.0);
        }
        alloc.note_off(note_id(1)).unwrap();

        // Drive through the 300ms release (≈ 13230 samples) with margin.
        let mut finished = false;
        for _ in 0..15000 {
            let (_, evs) = alloc.render_sample(44100.0, 0.0);
            if evs
                .iter()
                .any(|e| matches!(e, AllocatorEvent::VoiceFinished { .. }))
            {
                finished = true;
                break;
            }
        }
        assert!(finished, "expected VoiceFinished event after release");
        // Suppress unused variable warning.
        let _ = voice;
    }

    #[test]
    fn render_sample_sums_multiple_voices() {
        // Two voices playing the same note — sample should be louder than one.
        let mut alloc = VoiceAllocator::new(2);
        alloc.note_on(note_id(1), note_num(69), vel(0.5));
        alloc.note_on(note_id(2), note_num(69), vel(0.5));

        // Skip first sample (sin(0) == 0); take the second.
        alloc.render_sample(44100.0, 0.0);
        let mut one_voice = VoiceAllocator::new(1);
        one_voice.note_on(note_id(1), note_num(69), vel(0.5));
        one_voice.render_sample(44100.0, 0.0);

        let (two_sample, _) = alloc.render_sample(44100.0, 0.0);
        let (one_sample, _) = one_voice.render_sample(44100.0, 0.0);

        // Two voices should produce a louder signal.
        assert!(
            two_sample.abs() > one_sample.abs(),
            "two voices ({two_sample}) should be louder than one ({one_sample})"
        );
    }

    // ── Steal preference: Release before Attack/Decay/Sustain ─────────────────

    #[test]
    fn steal_prefers_releasing_voice_over_sustaining() {
        let mut alloc = VoiceAllocator::new(2);
        alloc.note_on(note_id(1), note_num(60), vel(0.5));
        alloc.note_on(note_id(2), note_num(62), vel(0.5));

        // Put voice 1 into Release.
        alloc.note_off(note_id(1)).unwrap();

        // Adding a third note should steal the releasing voice, emitting VoiceStolen
        // with old_note_id == 1.
        let events = alloc.note_on(note_id(3), note_num(64), vel(0.5));
        let stolen_old = events.iter().find_map(|e| {
            if let AllocatorEvent::VoiceStolen { old_note_id, .. } = e {
                Some(*old_note_id)
            } else {
                None
            }
        });
        assert_eq!(
            stolen_old,
            Some(NoteId::new(1)),
            "should steal the releasing voice"
        );
    }
}
