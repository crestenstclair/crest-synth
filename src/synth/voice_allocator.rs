/// Manages a fixed pool of voices and routes note commands to them.
///
/// # Steal policy
///
/// When all voices are active:
/// 1. Prefer voices in the `Release` stage (already dying).
/// 2. Among candidates in the same stage, steal the one with the lowest
///    envelope level (quietest) to minimise audible clicking.
///
/// # Examples
///
/// ```
/// use crest_synth::synth::voice_allocator::VoiceAllocator;
/// use crest_synth::kernel::note_id::NoteId;
/// use crest_synth::kernel::note_number::NoteNumber;
/// use crest_synth::kernel::velocity::Velocity;
///
/// let mut alloc = VoiceAllocator::new(4);
/// let note_id  = NoteId::new(1);
/// let note_num = NoteNumber::try_new(69).unwrap();
/// let vel      = Velocity::try_new(0.8).unwrap();
/// let events = alloc.note_on(note_id, note_num, vel);
/// assert!(!events.is_empty());
/// ```
pub struct VoiceAllocator {
    voices: Vec<Voice>,
}

impl VoiceAllocator {
    /// Create a new allocator with `num_voices` voices (capped at [`MAX_VOICES`]).
    ///
    /// # Panics
    ///
    /// Panics if `num_voices == 0`.
    pub fn new(num_voices: usize) -> Self {
        assert!(num_voices > 0, "num_voices must be at least 1");
        let count = num_voices.min(MAX_VOICES);
        let voices = (0..count).map(|_| Voice::new()).collect();
        Self { voices }
    }

    /// The number of voices in the pool.
    #[inline]
    pub fn voice_count(&self) -> usize {
        self.voices.len()
    }

    /// Number of voices currently sounding (not in the Idle stage).
    pub fn active_count(&self) -> usize {
        self.voices.iter().filter(|v| v.is_active()).count()
    }

    /// Apply a `NoteOn` command to the most suitable free or stealable voice.
    ///
    /// Returns the events generated by the affected voice (always at least one
    /// `AllocatorEvent::VoiceActivated`).
    pub fn note_on(
        &mut self,
        note_id: NoteId,
        note_number: NoteNumber,
        velocity: Velocity,
    ) -> Vec<AllocatorEvent> {
        let idx = self.pick_voice();
        let cmd = NoteOn {
            note_id,
            note_number,
            velocity,
        };
        let voice_events = self.voices[idx].note_on(cmd);
        self.translate_voice_events(idx, voice_events)
    }

    /// Apply a `NoteOff` command to the voice currently playing `note_id`.
    ///
    /// Returns `Err(AllocatorError::NoteNotFound)` if no voice owns that id.
    pub fn note_off(&mut self, note_id: NoteId) -> Result<AllocatorEvent, AllocatorError> {
        for (idx, voice) in self.voices.iter_mut().enumerate() {
            if voice.is_active() && voice.note_id() == note_id {
                let cmd = NoteOff { note_id };
                match voice.note_off(cmd) {
                    Ok(VoiceEvent::VoiceReleased { note_id: nid }) => {
                        return Ok(AllocatorEvent::VoiceReleased {
                            voice_index: idx,
                            note_id: nid,
                        });
                    }
                    Ok(_) => unreachable!("note_off only returns VoiceReleased on success"),
                    Err(_) => {
                        // Voice error — note was found but couldn't be released;
                        // treat as not-found from the allocator's perspective.
                        return Err(AllocatorError::NoteNotFound(note_id));
                    }
                }
            }
        }
        Err(AllocatorError::NoteNotFound(note_id))
    }

    /// Render one sample from every voice, collecting any `VoiceFinished` events.
    ///
    /// Returns the summed sample and a list of allocator events emitted this sample.
    ///
    /// `sample_rate` — audio sample rate in Hz (e.g. 44100.0).
    /// `semitone_detune` — pitch offset applied uniformly to all voices.
    ///
    /// # Audio-thread safety
    ///
    /// Lock-free and allocation-free at steady state.
    pub fn render_sample(
        &mut self,
        sample_rate: f64,
        semitone_detune: f64,
    ) -> (f32, Vec<AllocatorEvent>) {
        let mut sum = 0.0_f32;
        let mut events: Vec<AllocatorEvent> = Vec::new();

        for (idx, voice) in self.voices.iter_mut().enumerate() {
            let (sample, maybe_event) = voice.render_sample(sample_rate, semitone_detune);
            sum += sample;
            if let Some(VoiceEvent::VoiceFinished { note_id }) = maybe_event {
                events.push(AllocatorEvent::VoiceFinished {
                    voice_index: idx,
                    note_id,
                });
            }
        }

        (sum, events)
    }

    // ── Private helpers ───────────────────────────────────────────────────────

    /// Pick the best voice index for the next note-on.
    ///
    /// Priority:
    /// 1. Any voice in `Idle` stage (free, no steal).
    /// 2. A `Release`-stage voice with the lowest envelope level.
    /// 3. Any non-idle voice with the lowest envelope level.
    fn pick_voice(&self) -> usize {
        // 1. Free (idle) voice — take the first one found.
        for (idx, voice) in self.voices.iter().enumerate() {
            if voice.is_reclaimable() {
                return idx;
            }
        }

        // 2. Prefer Release-stage voices.
        let mut best_release: Option<(usize, f64)> = None;
        for (idx, voice) in self.voices.iter().enumerate() {
            if voice.envelope_stage() == EnvelopeStage::Release {
                let level = voice.envelope_level().value();
                let is_better = best_release.is_none_or(|(_, best)| level < best);
                if is_better {
                    best_release = Some((idx, level));
                }
            }
        }
        if let Some((idx, _)) = best_release {
            return idx;
        }

        // 3. Fall back to the quietest active voice.
        self.voices
            .iter()
            .enumerate()
            .min_by(|(_, a), (_, b)| {
                a.envelope_level()
                    .value()
                    .partial_cmp(&b.envelope_level().value())
                    .unwrap_or(std::cmp::Ordering::Equal)
            })
            .map(|(idx, _)| idx)
            .unwrap_or(0)
    }

    /// Convert `VoiceEvent`s from the voice aggregate into `AllocatorEvent`s.
    fn translate_voice_events(
        &self,
        voice_index: usize,
        events: Vec<VoiceEvent>,
    ) -> Vec<AllocatorEvent> {
        events
            .into_iter()
            .map(|ev| match ev {
                VoiceEvent::VoiceActivated {
                    note_id,
                    note_number,
                    ..
                } => AllocatorEvent::VoiceActivated {
                    voice_index,
                    note_id,
                    note_number,
                },
                VoiceEvent::VoiceStolen {
                    old_note_id,
                    new_note_id,
                } => AllocatorEvent::VoiceStolen {
                    voice_index,
                    old_note_id,
                    new_note_id,
                },
                VoiceEvent::VoiceReleased { note_id } => AllocatorEvent::VoiceReleased {
                    voice_index,
                    note_id,
                },
                VoiceEvent::VoiceFinished { note_id } => AllocatorEvent::VoiceFinished {
                    voice_index,
                    note_id,
                },
            })
            .collect()
    }
}

