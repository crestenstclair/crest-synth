//! Voice pool management with oldest/quietest steal policy.
//!
//! `VoiceAllocator` maintains a fixed array of [`Voice`] objects and routes
//! incoming `NoteOn`/`NoteOff` commands to them.  When all voices are active
//! the allocator steals the one with the lowest envelope level (quietest-first),
//! preferring Release-stage voices over Attack/Decay/Sustain voices.
//!
//! # Audio-thread safety
//!
//! `note_on` and `note_off` iterate over the voice array without heap
//! allocation or locking.  `render_block` is similarly lock-free and
//! allocation-free at steady state.

use crate::kernel::note_id::NoteId;
use crate::kernel::note_number::NoteNumber;
use crate::kernel::velocity::Velocity;
use crate::synth::envelope_stage::EnvelopeStage;
use crate::synth::voice::{NoteOff, NoteOn, Voice, VoiceEvent};

// ─────────────────────────────────────────────────────────────────────────────
// Public API types
// ─────────────────────────────────────────────────────────────────────────────

/// Maximum number of simultaneous voices supported by [`VoiceAllocator`].
///
/// A compile-time constant keeps the pool on the stack with no heap allocation.
pub const MAX_VOICES: usize = 16;

/// Events emitted by the allocator in response to note commands.
///
/// Callers may use these to drive UI feedback (active note count, steal
/// indicators, etc.).
#[derive(Debug, Clone, PartialEq)]
pub enum AllocatorEvent {
    /// A voice was assigned to a new note.
    VoiceActivated {
        voice_index: usize,
        note_id: NoteId,
        note_number: NoteNumber,
    },
    /// A voice that was sounding was stolen to play a different note.
    VoiceStolen {
        voice_index: usize,
        old_note_id: NoteId,
        new_note_id: NoteId,
    },
    /// A voice began its release phase (note-off received).
    VoiceReleased { voice_index: usize, note_id: NoteId },
    /// A voice finished sounding (envelope reached Idle).
    VoiceFinished { voice_index: usize, note_id: NoteId },
}

/// Error returned by [`VoiceAllocator::note_off`].
#[derive(Debug, Clone, PartialEq)]
pub enum AllocatorError {
    /// No voice is currently sounding the given `NoteId`.
    NoteNotFound(NoteId),
}

impl std::fmt::Display for AllocatorError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            AllocatorError::NoteNotFound(id) => {
                write!(f, "no active voice found for NoteId({id})")
            }
        }
    }
}

impl std::error::Error for AllocatorError {}

